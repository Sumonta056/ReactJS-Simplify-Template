
const month = ['January','February','March','April','May','June','July','August','September'];
const vvNumber = [31,30,29,33,32];

// Sort Number Ascending
console.log(vvNumber.sort());
/*
[ 29, 30, 31, 32, 33 ]
*/

// Sort Character
console.log(month.sort());
/*
[
  'April',     'August',
  'February',  'January',
  'July',      'June',
  'March',     'May',
  'September'
]
*/



